// Multiplication
const number1 = 2;
const number2 = 4;
const product = number1 * number2;

console.log('value of product:', product); //8
console.log('typeof product:', typeof product); //number

// Subtraction
const charge = 10;
const payment = 5;
const amountRemaining = charge - payment;

console.log('value of amountRemaining:', amountRemaining); //5
console.log('typeof amountRemaining:', typeof amountRemaining); //number

// Mean
const tests = 5;
const assignment = 10;
const final = 15;
const grade = (tests + assignment + final) / 3;

console.log('value of grade:', grade);
console.log('typeof grade:', typeof grade);

//Concatenate
const firstName = 'Jesus';
const lastName = 'Rodriguez-Luna';
const fullName = firstName + ' ' + lastName;

console.log('value of fullName:', fullName); //Jesus Rodriguez-Luna
console.log('typeof fullName:', typeof fullName); //string

// Boolean
const pH = 10;
const isAcidic = pH < 7; //false

console.log('value of isAcidic:', isAcidic); //false
console.log('typeof isAcidic:', typeof isAcidic); //boolean

// Boolean
const numberOfSOldiers = 300;
const isSparta = numberOfSOldiers === 300; //true

console.log('value of isSparta:', isSparta); //true
console.log('typeof isSparta:', typeof isSparta); //boolean

// String Concatenation
let nickname = fullName;
nickname += ' is the GOAT';

console.log('value of nickname:', nickname); // Jesus Rodriguez-Luna is the GOAT
